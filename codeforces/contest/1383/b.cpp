void solve() {
    int n;
    cin >> n;
    vi a(n);
    for (auto& i : a) cin >> i;

    for (int i = 31; i--;) {
        int cnt = 0;
        for (auto& x : a)
            if (1 << i & x) cnt++;
        if (cnt == 1) {
            print("WIN");
            return;
        } else if (cnt & 1) {
            print(cnt & n & 1 ? "LOSE" : "WIN");
            return;
        }
    }
    print("DRAW");
}
int main() {
    ios::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    // freopen("in", "r", stdin);

    int t = 1;
    cin >> t;
    while (t--) solve();

    return 0;
}

