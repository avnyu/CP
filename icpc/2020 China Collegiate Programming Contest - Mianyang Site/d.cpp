bool test(vi& a, int time) {
    int hav = time;
    for (auto& i : a) hav -= i < time ? time - i : 0;
    return hav >= 0;
}
void solve(int T) {
    int n;
    cin >> n;
    vi a(n);
    for (auto& i : a) cin >> i;

    int res = 0, add = 1 << 30;
    while (add) {
        if (test(a, res + add)) res += add;
        add >>= 1;
    }
    cout << "Case #" << T << ": " << res + 1 << '\n';
}
int main() {
    ios::sync_with_stdio(false), cin.tie(nullptr), cout.tie(nullptr);
    // freopen("in", "r", stdin);

    int t = 1;
    cin >> t;
    for (int i = 0; i++ < t;) solve(i);

    return 0;
}

